fn schema_document<T: JsonSchema>(filename: &'static str) -> Result<(&'static str, Vec<u8>)> {
    let mut document = serde_json::to_value(schema_for!(T))?;
    normalize_schema_meta(&mut document, filename)?;
    let mut bytes = serde_json::to_vec_pretty(&document)?;
    bytes.push(b'\n');
    Ok((filename, bytes))
}

fn normalize_schema_meta(document: &mut Value, filename: &str) -> Result<()> {
    if let Value::Object(object) = document {
        object.insert(
            "$schema".to_owned(),
            Value::String("https://json-schema.org/draft/2020-12/schema".to_owned()),
        );
    }
    add_explicit_integer_bounds(document);
    if filename == "certification-evidence.schema.json" {
        annotate_certification_evidence(document)?;
    }
    if filename == "certification-profile.schema.json" {
        annotate_certification_profile(document)?;
    }
    if filename == "compatibility-analysis.schema.json" {
        require_evidence_for_resolved_compatibility_assessments(document)?;
    }
    match filename {
        "execution-target-contract.schema.json" => annotate_execution_target_limits(document)?,
        "execution-resolution-evidence.schema.json" => {
            annotate_execution_resolution_limits(document)?;
        }
        _ => {}
    }
    Ok(())
}

fn annotate_certification_evidence(document: &mut Value) -> Result<()> {
    let root = document
        .as_object_mut()
        .context("certification evidence schema root is not an object")?;
    root.insert(
        "x-weregopher-maxDocumentBytes".to_owned(),
        json!(weregopher_domain::MAX_CERTIFICATION_DOCUMENT_BYTES),
    );
    let assessment = document
        .pointer_mut("/$defs/CertificationCheckAssessment")
        .and_then(Value::as_object_mut)
        .context("certification schema is missing its check-assessment definition")?;
    assessment.insert(
        "allOf".to_owned(),
        json!([{
            "if": {
                "properties": {
                    "status": { "const": "not_run" }
                },
                "required": ["status"]
            },
            "then": {
                "properties": {
                    "evidence": { "maxItems": 0 }
                }
            },
            "else": {
                "properties": {
                    "evidence": { "minItems": 1 }
                }
            }
        }]),
    );
    Ok(())
}

