/// Writes every canonical JSON Schema into `output_directory`.
///
/// # Errors
///
/// Returns an error when schema serialization or an output-directory write fails.
pub fn generate_schemas(output_directory: &Path) -> Result<()> {
    fs::create_dir_all(output_directory).with_context(|| {
        format!(
            "failed to create schema directory {}",
            output_directory.display()
        )
    })?;

    for (filename, bytes) in schema_documents()? {
        let path = output_directory.join(filename);
        fs::write(&path, bytes)
            .with_context(|| format!("failed to write generated schema {}", path.display()))?;
    }
    Ok(())
}

/// Verifies that checked-in schemas exactly match canonical generation.
///
/// # Errors
///
/// Returns an error when a schema is missing, unreadable, or differs from its
/// canonical generated bytes.
pub fn check_schemas(output_directory: &Path) -> Result<()> {
    for (filename, expected) in schema_documents()? {
        let path = output_directory.join(filename);
        let actual = fs::read(&path)
            .with_context(|| format!("generated schema is missing: {}", path.display()))?;
        if actual != expected {
            bail!(
                "generated schema is stale: {}; run `cargo xtask schema`",
                path.display()
            );
        }
    }
    Ok(())
}

/// Runs one repository automation command relative to `repository_root`.
///
/// # Errors
///
/// Returns an error when arguments do not name a supported command or the
/// selected schema operation fails.
pub fn run<I, S>(arguments: I, repository_root: &Path) -> Result<()>
where
    I: IntoIterator<Item = S>,
    S: AsRef<str>,
{
    let arguments: Vec<String> = arguments
        .into_iter()
        .map(|argument| argument.as_ref().to_owned())
        .collect();
    let schema_directory = repository_root.join("schemas");

    match arguments.as_slice() {
        [command] if command == "schema" => generate_schemas(&schema_directory),
        [command, flag] if command == "schema" && flag == "--check" => {
            check_schemas(&schema_directory)
        }
        _ => bail!("usage: cargo xtask schema [--check]"),
    }
}

