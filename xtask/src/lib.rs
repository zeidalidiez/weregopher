fn annotate_execution_resolution_limits(document: &mut Value) -> Result<()> {
    let root = document
        .as_object_mut()
        .context("execution resolution schema root is not an object")?;
    root.insert(
        "x-weregopher-maxDocumentBytes".to_owned(),
        json!(weregopher_domain::MAX_EXECUTION_RESOLUTION_DOCUMENT_BYTES),
    );
    root.insert(
        "x-weregopher-managedLocatorDigestEqualsExecutableDigest".to_owned(),
        Value::Bool(true),
    );
    Ok(())
}

fn require_evidence_for_resolved_compatibility_assessments(document: &mut Value) -> Result<()> {
    let assessment = document
        .pointer_mut("/$defs/DimensionAssessment")
        .and_then(Value::as_object_mut)
        .context("compatibility schema is missing its dimension-assessment definition")?;
    assessment.insert(
        "allOf".to_owned(),
        json!([{
            "if": {
                "properties": {
                    "status": {
                        "enum": ["satisfied", "unsatisfied", "not_applicable"]
                    }
                },
                "required": ["status"]
            },
            "then": {
                "properties": {
                    "evidence": {
                        "minItems": 1
                    }
                }
            }
        }]),
    );
    Ok(())
}

fn add_explicit_integer_bounds(value: &mut Value) {
    match value {
        Value::Object(object) => {
            let format = object
                .get("format")
                .and_then(Value::as_str)
                .map(str::to_owned);
            if let Some((minimum, maximum)) = format.as_deref().and_then(integer_bounds) {
                object.entry("minimum").or_insert(minimum);
                object.entry("maximum").or_insert(maximum);
            }
            for child in object.values_mut() {
                add_explicit_integer_bounds(child);
            }
        }
        Value::Array(values) => {
            for child in values {
                add_explicit_integer_bounds(child);
            }
        }
        _ => {}
    }
}

fn integer_bounds(format: &str) -> Option<(Value, Value)> {
    match format {
        "uint8" => Some((Value::from(0), Value::from(u8::MAX))),
        "uint16" => Some((Value::from(0), Value::from(u16::MAX))),
        "uint32" => Some((Value::from(0), Value::from(u32::MAX))),
        "uint64" => Some((Value::from(0), Value::from(u64::MAX))),
        "int8" => Some((Value::from(i8::MIN), Value::from(i8::MAX))),
        "int16" => Some((Value::from(i16::MIN), Value::from(i16::MAX))),
        "int32" => Some((Value::from(i32::MIN), Value::from(i32::MAX))),
        "int64" => Some((Value::from(i64::MIN), Value::from(i64::MAX))),
        _ => None,
    }
}
